#include <iostream>
using namespace std;

int main(){

// arithmetic operators (+,-,*,/,%)  
  int a=2/5;
  cout<<a<<endl;

  float b=2.0/5;
  cout<<b<<endl;

  double c=2.12/5;
  cout<<c<<endl<<endl;

//  relational operators (= , > , < , <= , >= , !=) 
  int x=5;
  int y=10;

  bool first=(a==b);
  cout<<first<<endl;

  bool second=(a>b);
  cout<<second<<endl;

  bool third=(a<b);
  cout<<third<<endl;

  bool fourth=(a<=b);
  cout<<fourth<<endl;

  bool fifth=(a>=b);
  cout<<fifth<<endl;

  bool sixth=(a!=b);
  cout<<sixth<<endl<<endl;

//   logical operators (&&and, ||or, !not)
  int d=0;
  cout<<!d<<endl;

}